import Foundation

public extension Style {
    enum CornerRadius {
        /// 32.0 3xl
        public static let xxxl: Double = 32.0
        /// 24.0 2xl
        public static let xxl: Double = 24.0
        /// 16.0 xl
        public static let xl: Double = 16.0
        /// 12.0 lg
        public static let lg: Double = 12.0
        /// 8.0 md
        public static let md: Double = 8.0
        /// 4.0 sm
        public static let sm: Double = 4.0
        /// 0.0 xs
        public static let xs: Double = 0.0
    }
}
